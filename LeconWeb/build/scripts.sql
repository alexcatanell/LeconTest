CREATE database lecon;

CREATE TABLE `cliente` (
  `ID` int(11) NOT NULL,
  `NAME` varchar(45) DEFAULT NULL,
  `EMAIL` varchar(45) DEFAULT NULL,
  `CPF` varchar(45) DEFAULT NULL,
  `CLASSE` int(11) DEFAULT NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `contrato` (
  `ID` int(11) NOT NULL,
  `IDCLIENTE` int(11) DEFAULT NULL,
  `IDSERVICO` int(11) DEFAULT NULL,
  `DTINICIO` date DEFAULT NULL,
  `DTFIM` date DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `cliente_fk_idx` (`IDCLIENTE`),
  KEY `servico_fk_idx` (`IDSERVICO`),
  CONSTRAINT `cliente_fk` FOREIGN KEY (`IDCLIENTE`) REFERENCES `cliente` (`ID`) ON DELETE SET NULL ON UPDATE SET NULL,
  CONSTRAINT `servico_fk` FOREIGN KEY (`IDSERVICO`) REFERENCES `servico` (`ID`) ON DELETE SET NULL ON UPDATE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `servico` (
  `ID` int(11) NOT NULL,
  `NAME` varchar(45) DEFAULT NULL,
  `DESCR` varchar(45) DEFAULT NULL,
  `VALOR` float DEFAULT NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;